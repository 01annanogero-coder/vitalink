// ── HELPER: get products by category ─────────────────────────────
export const getByCategory = (cat) =>
  cat === 'all' ? products : products.filter(p => p.category === cat);

// ── HELPER: get products by health goal ──────────────────────────
export const getByGoal = (goal) =>
  products.filter(p => p.healthGoals.includes(goal));

// ── HELPER: get featured products ────────────────────────────────
export const getFeatured = () =>
  products.filter(p => p.featured).slice(0, 4);

// ── HELPER: get product by slug ───────────────────────────────────
export const getBySlug = (slug) =>
  products.find(p => p.slug === slug);

// ── HELPER: get related products ─────────────────────────────────
export const getRelated = (product, limit = 3) =>
  products
    .filter(p => p.category === product.category && p.code !== product.code)
    .slice(0, limit);
